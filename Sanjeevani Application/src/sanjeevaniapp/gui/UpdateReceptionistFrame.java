    private void generateRecepIds() {
        jcRecepId.removeAllItems();
        try{
            List<String> RecepIdsList = ReceptionistDao.getAllRecepIds();
            for(String recepId : RecepIdsList){
                jcRecepId.addItem(recepId);
            }
            jcRecepId.setSelectedIndex(0);
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Error in DB");
            ex.printStackTrace();
        }
    }

    private boolean validateInputs() {
        loginId = txtLoginId.getText().trim();
        recepName = txtRecepName.getText().trim();
        char []pwd1 = txtPwd.getPassword();
        char []pwd2 = txtRePwd.getPassword();
        if(loginId.isEmpty() || pwd1.length == 0 || pwd2.length == 0 || recepName.isEmpty()){
            return false;
        }
        password1 = new String(pwd1).trim();
        password2 = new String(pwd2).trim();
        return true;
    }
    
    private String matchPassword(){
        if(password1.length()<3 || password2.length()<3)
            return "Passwords must be of atleast 3 characters in length";
        if(password1.equals(password2)==false)
            return "Passwords do not match";
        return null;
    }
