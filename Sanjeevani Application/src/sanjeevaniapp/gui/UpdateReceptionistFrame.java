    private void jcRecepIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcRecepIdActionPerformed
        try{
            
            String recepId = jcRecepId.getSelectedItem().toString();
            ReceptionistPojo recep = ReceptionistDao.getRecepbyId(recepId);
            txtRecepName.setText(recep.getReceptionistName());
            jcGender.setSelectedItem(recep.getReceptionistGender());
            
            
            UserPojo user = UserDao.getUserByName(txtRecepName.getText());
            txtLoginId.setText(user.getLoginId());
            txtPwd.setText(PasswordDecryption.getDecryptedPassword(user.getPassword()));
            txtRePwd.setText(PasswordDecryption.getDecryptedPassword(user.getPassword()));
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Error in DB");
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jcRecepIdActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if(validateInputs() == false){
            JOptionPane.showMessageDialog(null, "Please Input All Fields");
            return;
        }
        String result = matchPassword();
        if(result != null){
            JOptionPane.showMessageDialog(null, result);
            return;
        }
        try{
            
            ReceptionistPojo recep = new ReceptionistPojo(jcRecepId.getSelectedItem().toString(),txtRecepName.getText(),jcGender.getSelectedItem().toString());
            
            String encPassword = new String(PasswordEncryption.getEncryptedPassword(password1));
            UserPojo user = new UserPojo(loginId,recepName,encPassword,"Receptionist");
            
            boolean res1 = ReceptionistDao.updateRecep(recep);
            boolean res2 = UserDao.updateUser(user);
            
            if(res1 && res2){
                JOptionPane.showMessageDialog(null, "Receptionist Updated Successfully!");
                jcRecepId.setSelectedIndex(0);
                return;
            }
            JOptionPane.showMessageDialog(null, "Updation unsuccessful!"); 
            return; 
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Error in DB");
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

