    private void generateEmpIds(){
        jcEmpId.removeAllItems();
        try{
            List<String> empIdList = EmpDao.getAllEmpIds();
            for(String id : empIdList){
                jcEmpId.addItem(id);
            }
            jcEmpId.setSelectedIndex(0);
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error In DB"+ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }
    
    private boolean validateInputs() {
        empName=txtEmpName.getText().trim();
        empSal=txtEmpSal.getText().trim();
        if(empName.isEmpty()||empSal.isEmpty())
            return false;
        return true;
    }
