    private void btnUpdateEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateEmpActionPerformed
        boolean isValidInputs = validateInputs();
        if(!isValidInputs){
            JOptionPane.showMessageDialog(null, "Please input all the data!");
            return;
        }
        try{
            double eSal = Double.parseDouble(empSal);
            EmpPojo emp = new EmpPojo();
            emp.setEmpId(jcEmpId.getSelectedItem().toString());
            emp.setEmpDept(jcEmpJob.getSelectedItem().toString());
            emp.setEmpName(empName);
            emp.setEmpSal(eSal);
            
            boolean result = EmpDao.updateEmp(emp);
            if(result){
                JOptionPane.showMessageDialog(null, "Record Updated Successfully");
                jcEmpId.setSelectedIndex(0);
                if(UserProfile.getUserId().equalsIgnoreCase(emp.getEmpId())){
                    UserProfile.setUserName(empName);
                }
                return;
            }
            JOptionPane.showMessageDialog(null, "Record not Updated, Try Again !");
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error In DB"+ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
        catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null,"Please input numeric salary!"+ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnUpdateEmpActionPerformed

    private void jcEmpIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcEmpIdActionPerformed
        try{
            String empId = jcEmpId.getSelectedItem().toString();
            EmpPojo emp = EmpDao.getEmpbyEmpId(empId);
            txtEmpName.setText(emp.getEmpName());
            txtEmpSal.setText(String.valueOf(emp.getEmpSal()));
            jcEmpJob.setSelectedItem(emp.getEmpDept());
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error in DB");
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jcEmpIdActionPerformed

